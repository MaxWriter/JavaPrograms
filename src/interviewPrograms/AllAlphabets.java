package interviewPrograms;

public class AllAlphabets {
public static void main(String[] args) {

    System.out.print("Capital alphabets in english are : ");
    for(char c='A'; c<='Z';c++) {
        System.out.print(c+",");
    }
    System.out.println();
    System.out.print("Small alphabets in english are : ");
    for(char c='a'; c<='z';c++) {
        System.out.print(c+",");
    }
    System.out.println();
}
}
